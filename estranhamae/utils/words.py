def convert_to_ascii_string(text):
    lines = ["", "", "", "", "", ""]  # 6 linhas para as representações
    for char in text.upper():
        if char in ascii_art:
            art = ascii_art[char]
            for i in range(6):
                lines[i] += art[i]
    
    # Junta todas as linhas em uma única string separadas por nova linha
    return "\n".join(lines)

